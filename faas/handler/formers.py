    def predict(self, history, predict_window, extra_parameters=None):

        pred_data, pred_loader = self._get_data(numpy_data=np.array(history), flag='pred')

        preds = []

        self.model.eval()
        with torch.no_grad():
            for i, (batch_x, batch_y, batch_x_mark, batch_y_mark) in enumerate(pred_loader):
                batch_x = batch_x.float().to(self.device)
                batch_y = batch_y.float()
                batch_x_mark = batch_x_mark.float().to(self.device)
                batch_y_mark = batch_y_mark.float().to(self.device)

                # decoder input
                dec_inp = torch.zeros([batch_y.shape[0], self.args.pred_len, batch_y.shape[2]]).float().to(batch_y.device)
                dec_inp = torch.cat([batch_y[:, :self.args.label_len, :], dec_inp], dim=1).float().to(self.device)
                # encoder - decoder
                if self.args.use_amp:
                    with torch.cuda.amp.autocast():
                        if 'Linear' in self.args.model:
                            outputs = self.model(batch_x)
                        else:
                            if self.args.output_attention:
                                outputs = self.model(batch_x, batch_x_mark, dec_inp, batch_y_mark)[0]
                            else:
                                outputs = self.model(batch_x, batch_x_mark, dec_inp, batch_y_mark)
                else:
                    if 'Linear' in self.args.model:
                        outputs = self.model(batch_x)
                    else:
                        if self.args.output_attention:
                            outputs = self.model(batch_x, batch_x_mark, dec_inp, batch_y_mark)[0]
                        else:
                            outputs = self.model(batch_x, batch_x_mark, dec_inp, batch_y_mark)
                pred = outputs.detach().cpu().numpy()  # .squeeze()
                preds.append(pred)

        preds = np.array(preds)
        preds = preds.reshape(-1, preds.shape[-2], preds.shape[-1])

        # wzj
        # print(f"preds.shape:{preds.shape}")
        assert preds.shape[0] == 1 and preds.shape[2] == 1
        
        reverse_pred = pred_data.inverse_transform(preds.reshape(-1,1))
        reverse_pred_list = reverse_pred.reshape(-1,)
    
        # print(f"predict reverse_pred_list:{reverse_pred_list}")
        # print(f"shape of reverse_pred_list: {reverse_pred_list.shape}")

        predict_result = reverse_pred_list.tolist()[0:predict_window]

        assert predict_window == len(predict_result)

        return predict_result



